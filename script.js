// Fibonacci-based delay easing
function fibonacciEase(index) {
  const fib = [0, 1];
  for (let i = 2; i <= index + 2; i++) {
    fib[i] = fib[i - 1] + fib[i - 2];
  }
  return 0.3 + (fib[index + 1] % 6) * 0.1;
}

