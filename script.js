/* -------------------------------
   Light/Dark Mode Toggle
-------------------------------- */
const toggleButton = document.getElementById('theme-toggle');

toggleButton.addEventListener('click', () => {
  const isDark = document.documentElement.getAttribute('data-theme') === 'dark';

  if (isDark) {
    document.documentElement.removeAttribute('data-theme');
    toggleButton.setAttribute('aria-pressed', 'false');
    document.documentElement.style.setProperty('--dark', 0);
    document.documentElement.style.setProperty('--cloud', 'hsl(204, 53%, 47%)');
    document.documentElement.style.setProperty('--stars', 'hsl(0, 0%, 95%)');
  } else {
    document.documentElement.setAttribute('data-theme', 'dark');
    toggleButton.setAttribute('aria-pressed', 'true');
    document.documentElement.style.setProperty('--dark', 1);
    document.documentElement.style.setProperty('--cloud', 'hsl(210, 30%, 20%)');
    document.documentElement.style.setProperty('--stars', 'hsl(0, 0%, 95%)');
  }
});

