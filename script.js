// === True Infinite Partner Carousel ===

const partnersTrack = document.querySelector('.partners-track');
const partnersLogos = document.querySelectorAll('.partners-track img');
let partnerScrollSpeed = 0.5;
let partnerPosition = 0;

function animatePartners() {
  partnerPosition -= partnerScrollSpeed;
  
  if (Math.abs(partnerPosition) >= partnersLogos[0].clientWidth + 64) {
    const firstPartnerLogo = partnersTrack.firstElementChild;
    partnersTrack.appendChild(firstPartnerLogo);
    partnerPosition += partnersLogos[0].clientWidth + 64;
  }

  partnersTrack.style.transform = `translateX(${partnerPosition}px)`;
  requestAnimationFrame(animatePartners);
}

animatePartners();

